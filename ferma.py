import math
print("Метод факторизации Ферма")
n = int(input("Введите число, которое надо факторизовать: "))
s = math.ceil(n ** 0.5)
k=0
L=((((s+k)**2)-n)**0.5)
while ((str(L)[len(str(L))-1])!='0') and k<10000000:
    k += 1
    L=((((s+k)**2)-n)**0.5)
if(k!=10000000):
    a = s+k+int(L)
    b = (s+k)-int(L)
    print(n,"раскладывается на числа:",a,"и",b)
else:
    print("Ответ не найден!")
